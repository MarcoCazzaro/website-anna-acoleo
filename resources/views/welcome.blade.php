                <h2>Crescendo in musica</h2>
                <div class="CLSraggruppamentosezione">
                        <div class="CLScrescendo">
                            <div class="CLScrescendosfondo CLScrescendosfondo1"></div>
                            <div class="CLScrescendocitazione">
                                <q>La vita senza musica sarebbe un errore.</q><br />
                                Friedrich NIETZSCHE
                            </div>
                        </div>
                        <div class="CLScrescendo">
                            <div class="CLScrescendotesto">
                                Ho creato “Crescendo in musica” perché credo fermamente nelle potenzialità educative della musica.<br />
                                La musica è fonte di gioia per i bambini, li arricchisce interiormente perché dà loro la possibilità di 
                                esprimersi con spensieratezza attraverso uno strumento musicale, nel mio caso il pianoforte, ma anche 
                                attraverso il canto e il gioco. Libera la fantasia perché ogni bambino può inventare una interpretazione diversa della musica che vuole eseguire.
                            </div>                          
                        </div>
                        <div class="CLScrescendo">
                            <div class="CLScrescendosfondo CLScrescendosfondo2"></div>
                            <div class="CLScrescendocitazione">
                                <q>Non è perché le cose sono difficili che non osiamo, è perché non osiamo che sono difficili.</q><br />
                                Lucio Anneo SENECA
                            </div>
                        </div>
                        <div class="CLScrescendo">
                            <div class="CLScrescendotesto">
                                Creiamo assieme delle fiabe in cui parole e musica si intrecciano unendo i piccoli pianisti 
                                in un’unica avventura che trova realizzazione nel saggio di fine anno.<br />
                                L’obiettivo non è solo insegnare a leggere le note e suonare dei tasti. 
                                E’ imparare ad ascoltare e ascoltarsi, acquisire pazienza e dedizione. 
                                Cerco di trarre dal bambino le peculiarità che lo rendono unico, capace e meraviglioso. 
                                Sviluppiamo assieme un linguaggio non verbale fatto di suoni, di espressività, di sentimenti.
                            </div>                          
                        </div>
                        <div class="CLScrescendo">
                            <div class="CLScrescendosfondo CLScrescendosfondo3"></div>
                            <div class="CLScrescendocitazione">
                                <q>La vera fatica che l’adulto è chiamato a compiere nei riguardi del bambino non è l’abbassarsi, il farsi piccolo davanti a lui ma l’innalzarsi fino al suo livello.</q><br />
                                Janusz KORCZAK
                            </div>
                        </div>
                        <div class="CLScrescendo">
                            <div class="CLScrescendotesto">
                                La musica ha il potere di migliorare l’apprendimento dei bambini. Associata al movimento aiuta 
                                lo sviluppo della capacità di coordinazione, di concentrazione e di interazione sociale.<br />
                                Attraverso il lavoro di gruppo il bambino impara a relazionarsi con gli altri. Attraverso il 
                                lavoro individuale impara a rapportarsi con se stesso, conoscere i propri limiti, superarli 
                                e affrontare timori e insicurezze.<br />
                                Chi si avvicina alla musica riesce poi ad avere un miglior rendimento scolastico.
                            </div>                          
                        </div>
