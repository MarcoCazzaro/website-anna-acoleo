                <h2>Curriculum</h2>
                <article class="CLSarticolocurr CLSarticolocurr1">
                    <div class="CLSarticolotesto">
                    <strong>Nata</strong> a Castelfranco Veneto, inizia lo studio del pianoforte all’età di cinque anni, diplomandosi brillantemente presso il Conservatorio della sua cittadina.
                    <br />Segue i corsi di perfezionamento con Mario Brunello, presso la Scuola Superiore Internazionale di Musica da Camera del Trio di Trieste, ed alla Scuola di Musica di Fiesole.</div>
                </article>
                <article class="CLSarticolocurr CLSarticolocurr2">
                    <div class="CLSarticolotesto">
                    <strong>Vincitrice</strong> in duo pianistico con la sorella Paola di numerosi concorsi nazionali e internazionali:
                    <br />- Primo Premio Assoluto al concorso nazionale di Osimo
                    <br />- Primo Premio al “Franz Schubert”
                    <br />- Primo Premio Assoluto all’Europeo di Moncalieri - vincitrice della Selezione “ARAM”
                    <br />- Primo Premio al “Francois Grimaldi” di Parma
                    <br />- Terzo Premio all’Internazionale Soliva e all’Internazionale “Perugia Classico” e “Sanremo Classico”
                    <br />fino al prestigioso riconoscimento alla prima edizione del Concorso Internazionale “Premio Trio di Trieste” come unico duo pianistico premiato tra concorrenti di 22 nazionalità diverse</div>
                </article>
                <article class="CLSarticolocurr CLSarticolocurr3">
                    <div class="CLSarticolotesto">
                    <strong>Si dedica</strong> all’attività concertistica di musica da camera nelle principali città italiane (Padova, Verona, Milano, Firenze, Bologna, Roma, Ravello) e in prestigiose sedi straniere, quali la Musikhalle di Amburgo, la Sala del Museo Chopin di Varsavia, e ancora Danzica, Lubek, Kiel, Olsztyn, riscuotendo sempre ampio consenso di pubblico e di critica.</div>
                </article>
                <article class="CLSarticolocurr CLSarticolocurr4">
                    <div class="CLSarticolotesto">
                    <strong>E’ stata ospite</strong> a Rai Tre nella trasmissione di Corrado Augias “Le Storie” in una puntata dedicata a Franz Schubert.</div>
                </article>
                <article class="CLSarticolocurr CLSarticolocurr5">
                    <div class="CLSarticolotesto">
                    <strong>Ha suonato</strong> per Asolo Musica, gli Amici della Musica di Firenze, la Società Beethoven di Crotone, il Festival Internazionale di Musica di Monfalcone, gli Amici della Lirica di Verona, l’Istituzione Musicale Villa Contarini, gli Amici della Musica di Lanciano, il Bologna Festival, la Società Umanitaria di Milano, il Rossano Opera Festival etc..</div>
                </article>
                <article class="CLSarticolocurr CLSarticolocurr6">
                    <div class="CLSarticolotesto">
                    <strong>Ama</strong> intrecciare musica, danza, teatro e poesia, collaborare con attori, ballerini e registi, confrontarsi in tematiche di attualità, cercando di riproporre la musica “classica” in nuove e suggestive dinamiche.</div>
                </article>
                <article class="CLSarticolocurr CLSarticolocurr7">
                    <div class="CLSarticolotesto">
                    <strong>Laureata</strong> in architettura, dopo anni dedicati alla professione, attualmente si dedica all’attività di insegnante divertendosi con i bambini ad esplorare il fantastico mondo del pianoforte con un approccio alternativo alla tradizionale didattica dello strumento.</div>
                </article>
                <article class="CLSarticolocurr CLSarticolocurr8">
                    <div class="CLSarticolotesto">
                    <strong>Dal 2008</strong> suona in duo con la violoncellista Anna Campagnaro.</div>
                </article>
            </div>
